
function clickMenu() {
    if (itens.style.display == 'block') {
        itens.style.display = 'none'
    } else {
        itens.style.display = 'block'
    }
}

function clickCart() {
    if (msgcarrinho.style.display == 'block') {
        msgcarrinho.style.display = 'none'
    } else {
        msgcarrinho.style.display = 'block'
    }
}